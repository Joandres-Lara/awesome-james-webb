const { diffTime } = require("../src/helpers");

describe("diffTime helper", () => {
 test.each([
  [1, "00:00:00.001"],
  [999, "00:00:00.999"],
  [1000, "00:00:01.000"],
  [9999, "00:00:09.999"],
  [60000, "00:01:00.000"],
  [3600000, "01:00:00.000"],
  [5000, "00:00:05.000"],
  [9781234, "02:43:01.234"],
  [9781234 + 10000, "02:43:11.234"],
  [9781234 + 10000 + (60*60*1000*5), "07:43:11.234"],
  [1000 * 60 * 60 * 365 * 2 + 10000, "2.00:00:10.000"],
 ])("time: %d, expect string: %s", (time, expectString) => {
  expect(diffTime(time)).toEqual(expectString);
 })
})
